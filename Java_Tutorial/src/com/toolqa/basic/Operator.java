package com.toolqa.basic;

public class Operator {

	public static void main(String[] args) {

		int a, b, c;
		b = 10;
		c = 5;

		// For Additional
		a = b + c;
		System.out.println("Additional :" + a);
		
		//Substraction 
		a=b-c;
		System.out.println("Substraction :"+a);
		
		// Multiplication 
		a= b*c;
		System.out.println("multiplication :"+a);
		
		// Divide
		a= b/c;
		System.out.println("Divide :"+a);

	}

}
