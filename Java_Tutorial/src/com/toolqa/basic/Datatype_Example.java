package com.toolqa.basic;

public class Datatype_Example {
	public static void main(String[] args) {

		// Data Type: INTEGER
		int carspeed;
		carspeed = 20;
		
		// Print the Car Speed
		System.out.println("The speed of the Car is :" + " " + carspeed);

		carspeed = carspeed + 20;
		System.out.println(carspeed);

		// Data Type: CHAR
		char grade;
		grade = 'A';
		
		// Print the Grade
		System.out.println("The Grade of the student is : " + grade);
		
		// Data Type: DOUBLE
		double salary;
		salary = 10989.98;
		// Print the salary 
		System.out.println("The salary is : "+salary);

	}

}
